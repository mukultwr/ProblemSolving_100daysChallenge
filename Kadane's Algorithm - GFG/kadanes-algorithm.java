class Solution{

    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long maxSubarraySum(int arr[], int n){
        
        int sum=arr[0];
        int curSum=arr[0];
        if(n==1){
            return arr[0];
        }
        for(int i=1;i<arr.length;i++){
            curSum=Math.max(curSum+arr[i],arr[i]);
            sum=Math.max(curSum,sum);
        }
        return sum;
    }
    
}

